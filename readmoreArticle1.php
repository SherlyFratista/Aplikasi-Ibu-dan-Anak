    		</div>
    		<div>
    			<label> <b> Kolin </b> sangat penting selama kehamilan untuk perkembangan otak janin, dan tetap menjadi salah satu nutrisi paling penting untuk otak sepanjang hidup manusia.
				Peranan <b> kolin </b> dalam mendukung kepintaran anak adalah dengan membantu meningkatkan sinyal saraf di otak, sehingga memori otak dapat terjaga dengan baik.
				Kuning telur adalah salah satu sumber <b> kolin </b> yang cukup tinggi, tetapi Mam juga bisa mendapatkannya di kecambah, daging sapi, unggas, dan edamame. </label>
    		</div>
