    		</div>
    		<div>
    			<label> Zat besi merupakan mineral vital yang diperlukan oleh tubuh untuk meningkatkan kecerdasan anak. Hal ini karena zat besi merupakan kendaraan dari sel darah merah untuk menjalankan fungsinya, mengangkut oksigen ke seluruh tubuh, termasuk otak.
				Untuk mendukung kecerdasan otak, asupan oksigen di dalam otak harus terus terpenuhi, sehingga konsentrasi anak dapat terus terjaga.
				Kekurangan zat besi dapat menurunkan kemampuan berpikir serta berbahasa anak. Mam bisa mendapatkan zat besi dari daging merah, kacang-kacangan, biji–bijian, sereal, bayam, tomat, brokoli, dan stroberi.
				Nah, selain memastikan asupan <b> nutrisi otak anak </b> untuk membantu perkembangan otak anak yang optimal, Mam juga dapat membantu mendukung kehebatan anak dengan aneka stimulasi berupa kegiatan yang tepat, misalnya bermain musik, belajar bahasa, mengajaknya memelajari hal-hal baru, serta melakukan aktivitas fisik yang membantu dukung potensi belajar si Kecil agar #DariBelajarJadiHebat. </label>
