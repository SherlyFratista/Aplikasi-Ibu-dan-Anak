    		<div>
    			<label class ="LabelHeader3"> <b> Asam lemak omega-3 </b> </label>
    		</div>
    		<div>
    			<label> DHA (Docosahexaenoic acid) adalah bentuk dari asam lemak omega-3 yang ditemukan berlimpah di sel-sel otak. Itu sebabnya, ikan sebagai salah satu sumber DHA yang paling kaya, disebut sebagai makanan otak. Selain DHA, jenis asam lemak omega-3 lainnya adalah ALA (Alpha-linolenic acid) yang berfungsi meningkatkan membran sel di dalam tubuh dan membantu meningkatkan daya tahan tubuh, dan EPA (Eicosapentaenoic acid) yang berfungsi sebagai zat anti-radang dan membantu menurunkan kadar kolesterol jahat di dalam tubuh.
				Faktanya, penelitian yang dilakukan University of Oxford menunjukkan bahwa anak-anak yang mendapat lebih banyak omega-3, khususnya DHA, mendapat nilai lebih tinggi pada tes membaca dan mengeja, serta memiliki lebih sedikit masalah perilaku dibandingkan anak-anak yang tidak mendapat omega-3. 
				Omega-3 sendiri disebut sebagai asam lemak esensial karena harus didapat dari makanan. Sumber utamanya adalah ikan, terutama varietas ikan berminyak seperti salmon dan sarden. Meski begitu, ikan secara umum dapat membantu meningkatkan kesehatan otak, karena sebuah studi dari UCLA menemukan bahwa mereka yang makan ikan panggang — bukan ikan goreng — setidaknya sekali seminggu, diasosiasikan dengan volume sel abu-abu yang lebih besar di area otak yang bertanggung jawab dalam memori dan kognitif. </label>
    		</div>
    		<div>
    			<label class ="LabelHeader3"> <b> Asam lemak omega 6 </b> </label>
    		</div>
    		<div>
    			<label> Seperti asam lemak omega-3, asam lemak omega-6 adalah asam lemak esensial yang harus didapat dari makanan. Jenis asam lemak omega-6 yang paling umum adalah linoleic acid (LA) atau asam linoleat, yang dapat diubah menjadi lemak omega-6 yang lebih panjang seperti AA (Arachidonic acid), atau ada pula yang menyingkatnya ARA.
				AA banyak dijumpai pada membran sel, dan merupakan senyawa yang penting dalam komunikasi antarsel serta menjadi senyawa prekursor (penyusun) bagi senyawa-senyawa penting lainnya dalam tubuh. Fungsi AA di antaranya mendukung pembentukan otak, jaringan saraf, jaringan penglihatan, serta membantu pembentukan sistem imun. </label>
    		</div>
    		<div>
